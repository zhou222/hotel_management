package com.jnu.pojo;

public class worker {
	private String workerid;
	private String workername;
	private String workersex;
	private String workeridnumber;
	private String workerposition;
	private int workerwage;
	private String workerpassword;
	public String getWorkerid() {
		return workerid;
	}
	public void setWorkerid(String workerid) {
		this.workerid = workerid;
	}
	public String getWorkername() {
		return workername;
	}
	public void setWorkername(String workername) {
		this.workername = workername;
	}
	public String getWorkersex() {
		return workersex;
	}
	public void setWorkersex(String workersex) {
		this.workersex = workersex;
	}
	public String getWorkeridnumber() {
		return workeridnumber;
	}
	public void setWorkeridnumber(String workeridnumber) {
		this.workeridnumber = workeridnumber;
	}
	public String getWorkerposition() {
		return workerposition;
	}
	public void setWorkerposition(String workerposition) {
		this.workerposition = workerposition;
	}
	public int getWorkerwage() {
		return workerwage;
	}
	public void setWorkerwage(int workerwage) {
		this.workerwage = workerwage;
	}
	public String getWorkerpassword() {
		return workerpassword;
	}
	public void setWorkerpassword(String workerpassword) {
		this.workerpassword = workerpassword;
	}
}
